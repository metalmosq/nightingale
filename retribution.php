			<p>"I bought a few instruments that would inspire me, and eventually the riffs started piling up," Swan&ouml; recalls. "I was
collecting them for some kind of death metal release, and the other stuff that came out ended up being what could be
used for a future Nightingale record."</p>

			<p>Originally titled &#8220;Bravado&#8221; in the working stages, &#8220;Retribution&#8221; offers up 10 songs steeped in uncomplicated '70s and
early '80s-flavoured rock. Tracks such as 'Chasing The Storm Away', 'Forevermore' and 'The Maze' could have easily
found a home on commercial rock radio 30 years ago, yet the album is completely relevant in 2014. Fans of Swan&ouml;'s
heavier works that are unfamiliar with Nightingale may be surprised the simplicity of the music and the band's non-
aggressive approach.</p>

			<p class="quote-area">
				<span class="quote">Wow... Nightingale managed to write and record some catchy tunes here! The production is great as expected. Love the synth. And of course... that voice...Seems like Dan is only getting better with the years!</span>
				<span class="author">Arjen Lucassen (Ayreon)</span>
			</p>

			<p>"It's not easy to write simple stuff that's good," Swan&ouml; points out, suggesting people take a good long listen to
&#8220;Retribution&#8221; rather than dismissing it.</p>

			<p>In Swan&ouml;'s estimation &#8220;Retribution&#8221; succeeds because the songs "just kind of happened." He never set out to write any
specific parts; the music is in fact a result of spontaneous moments, whether it was an accidental combination of notes
on a keyboard that became an opening riff ('On Stolen Wings') or an odd guitar tuning ('Warriors Of The Dawn'). On top
of that, the songs were hashed out in the rehearsal room before the band went into the studio, resulting in major
changes to some of the music as it developed.</p>



			<p>"When I listen to the record I don't want to have any regrets," explains Swan&ouml;. "There's no point in releasing a new
Nightingale record if I don't think it's the best we ever did. That a pretty high standard to have, but if I don't feel that
way when I listen to it the moment it's ready, it's got nothing to do with our back catalogue. That's the way I've felt with
every record."</p>

			<p class="quote-area">
				<span class="quote">This is Dan Swan&ouml; and co. at their most heartfelt, melodic and passionate. A new classic!</span>
				<span class="author">Mikael Stanne (Dark Tranquillity)</span>
			</p>

			<p>Asked to sum up what &#8220;Retribution&#8221; means to him with regards to Nightingale's legacy, Swan&ouml; offers the following:</p>

			<p>"Classic rock with that pomp attitude really inspired me. I just wanted a good production that could hold up well against
a band like Alter Bridge but still have a bit of the sonic charisma of the records from '79, which was a great year for
music. The target was to make a timeless record with good, classy songs that the four of us can agree are really cool."</p>

			<p>Nightingale's &#8220;Retribution&#8221; comes packaged in beautiful artwork courtesy of Travis Smith (Opeth, Nevermore,
Katatonia, etc.) and should equally appeal to open-minded atmospheric metal and melodic prog rock supporters into
bands like Rush, Marillion, Styx, Kansas, The Mission, Queensryche, Enchant, Threshold, Arena and even Opeth or
Katatonia.</p>


