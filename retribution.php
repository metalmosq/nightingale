		<div class="press-release">
			<p>RETRIBUTION is the new album from Nightingale, the intended one-off project that refuses to die. Established by musical
multi-talent Dan Swan&ouml; almost 20 years ago, the band is proof that good music can take on a life of its own, often when
the artist least expects it.</p>

			<p>Known for his work both as a producer/engineer and with metal acts Edge Of Sanity, Bloodbath, Pan-Thy-Monium and
most recently Witherscape, Swan&ouml; began his unplanned Nightingale journey in 1995 with &#8220;The Breathing Shadow&#8221;. It
was a one-off goth-flavoured solo album heavily reminiscent of The Sisters Of Mercy, meant to satisfy his interest in the
genre and then be put quietly to bed as Swan&ouml; moved on to other projects. The album was successful enough to
warrant a follow-up according to his label at the time (Black Mark), but Swan&ouml; was, as he puts it "so over the goth
thing."</p>
			<p class="quote-area">
				<span class="quote">As far as I'm concerned Dan Swan&ouml; is one of my earliest mentors. He's one of the most musical persons I know and has taught me a lot, especially when we were working together. Been following Nightingale with interest. His evolution as a vocalist and songwriter has really come to life in this band more than in any of his other projects. My favorite traveling music together with The Who...And he doesn't even know this! Very excited about this new Nightingale opus!</span>
				<span class="author">Mikael &Aring;kerfeldt (Opeth)</span>
			</p>

			<p>"I thought that if I was going to make a second record it had to reflect what I was listening to at the moment. I was going
through a big revival of Gamma, Foreigner, Journey and all that super melodic AOR pomp rock stuff. It was a weird turn
from the first record, so I decided to make Nightingale a home for music that I write in the moment, no matter what it
is."</p>

			<p>Nightingale released five more albums between '96 and '07, slowly establishing a band line-up that began with
Swan&ouml;'s guitarist/keyboardist brother Dag in 1996 acting as a co-producer and session player on &#8220;The Closing
Chronicles&#8221;. He officially came aboard in 1998 under his Tom Nouga moniker. The band was fleshed out by bassist Erik
Oskarsson and drummer Tom Bj&ouml;rn, who had their first rehearsal with the Swan&ouml; brothers on Christmas Day 2000.
&#8220;White Darkness&#8221; from 2007 could well have been the last Nightingale album, as it featured very little songwriting input
from Swan&ouml; due to severe writer's block. He decided to focus on his career as an engineer and chose to make music as
a hobby. His creative side won eventually, however, as the urge to write and play again became irresistible.</p>

<div class="lineup">
	<img src="images/ng-lineup.jpg" alt="The Nightingale Line-up">
